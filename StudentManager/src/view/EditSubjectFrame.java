    private void showSubject() {
        subjectList = SubjectModify.findAll();
        
        tableModel1.setRowCount(0);
        
        subjectList.forEach(subject -> {
            tableModel1.addRow(new Object[] {tableModel1.getRowCount() + 1, subject.getSid(),subject.getSname(),
                subject.getCredit(), subject.getWeight(), subject.getSchool(), subject.getLecturer(), subject.getCredit(), subject.getWeight()});
        });
    }

