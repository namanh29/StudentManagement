    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        sbj.setSname(txtSName.getText());
        sbj.setSchool(cbSchool.getSelectedItem().toString());
        sbj.setLecturer(txtLecturer.getText());
        try{
            if(Integer.parseInt(txtCredit.getText()) > 0)
                sbj.setCredit(Integer.parseInt(txtCredit.getText()));
            else {
                JOptionPane.showMessageDialog(this, "Số tín chỉ không hợp lệ...");
                return;
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Số tín chỉ không hợp lệ...");
            return;
        }
        try{
            if(Float.parseFloat(txtWeight.getText()) > 0 && Float.parseFloat(txtWeight.getText()) < 1)
                sbj.setWeight(Float.parseFloat(txtWeight.getText()));
            else{
                JOptionPane.showMessageDialog(this, "Trọng số QT không hợp lệ...");
                return;
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Trọng số QT không hợp lệ...");
            return;
        }
        
        if(sbj.getSname().equals("")){
            JOptionPane.showMessageDialog(this, "Tên học phần không được bỏ trống...");

        }
        else{
            JOptionPane.showMessageDialog(this, "Sửa thành công...");
            SubjectModify.update(sbj);
        }

        showSubject();

    }//GEN-LAST:event_btnSaveActionPerformed

